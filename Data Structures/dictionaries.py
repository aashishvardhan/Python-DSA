dictionary = {1 : "Aashish", 2 : "Vardhan"}

print(dictionary)
# We can directly access by passing the value
print(dictionary[1])

# We access the values using get function by passing in the keys
print(dictionary.get(1))

# We can use dictionaries as key value pairs. They are now ordered.
# To access all the functions of the dictionary try out . operator after the dictionary