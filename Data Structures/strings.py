# Python Strings is the immutable array of bytes representing Unicode characters. 
# Python does not have a character data type, a single character is simply a string with a 
# length of 1.

string = "I am a good boy"
# As the strings are immutable, we can just access the elements(characters) but we can't change them
# As strings are immutable, modifying a string will result in creating a new copy.

print(string[0])
print(string[-1])